# Escape Sequence

# \  is called escape sequence used when we want to print quotation under quotation

text = "this is 'python' Tutorial"
print(text)

text2 = 'this is "line no. 44" of code '
print(text2)

text3 = 'this is "python\" tutorial'
print(text3)

text5 = "this is \n python \n tutorial"
print(text5)

